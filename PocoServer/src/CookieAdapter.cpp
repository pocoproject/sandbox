#include "Poco/Servlet/PocoServer/CookieAdapter.h"
#include "Poco/Net/HTTPCookie.h"
#include "Poco/Servlet/Cookie.h"


using Poco::Net::HTTPCookie;
using Poco::Servlet::Cookie;


namespace Poco {
namespace Servlet {
namespace PocoServer {


HTTPCookie CookieAdapter::cookie(const Cookie& cookie) const
{
	HTTPCookie netCookie(cookie.getName(), cookie.getValue());
	
	netCookie.setComment(cookie.getComment());
	netCookie.setDomain(cookie.getDomain());
	netCookie.setMaxAge(cookie.getMaxAge());
	netCookie.setPath(cookie.getPath());
	netCookie.setSecure(cookie.getSecure());
	netCookie.setVersion(cookie.getVersion());

	return netCookie;
}


Cookie CookieAdapter::cookie(const HTTPCookie& cookie) const
{
	Cookie servletCookie(cookie.getName(), cookie.getValue());
	
	servletCookie.setComment(cookie.getComment());
	servletCookie.setDomain(cookie.getDomain());
	servletCookie.setMaxAge(cookie.getMaxAge());
	servletCookie.setPath(cookie.getPath());
	servletCookie.setSecure(cookie.getSecure());
	servletCookie.setVersion(cookie.getVersion());

	return servletCookie;
}


} } } // namespace Poco::Servlet::PocoServer
