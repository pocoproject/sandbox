#include "samplePimpl.h"

class ImplementationDetails
{

public:

    ImplementationDetails()
    {
        std::cout << "Create object for implementation datails\n";
    }

    ~ImplementationDetails()
    {
        std::cout << "Destory implementation object\n";
    }

    void bar(){
        std::cout << "ImplementationDetails::bar(): doing something private\n";
    }

};


PublicInterface::PublicInterface() : _pimpl(new ImplementationDetails)
{
}

PublicInterface::~PublicInterface() 
{
    // actually do nothing, since we use Poco::SharedPtr
}

void PublicInterface::foo() 
{
    // dispatch to implementation details, here call ImplementationDetails::bar
    _pimpl->bar();
}

// --------------------------------  user code -------------------------------

int main()
{
    PublicInterface obj;
    obj.foo();
}


