#include "Poco/SharedArray.h"
#include <assert.h>
#include <iostream>

using Poco::SharedArray;
using Poco::NullPointerException;

namespace
{
    class TestObject
    {
    public:
        TestObject(const int& data = 0) : _data(data)
        {
            ++_count;
            std::cout << "current counter : " << _count << "\n";
        }

        virtual ~TestObject()
        {
            --_count;
            std::cout << "current counter : " << _count << "\n";
        }

        void set(const int& data)
        {
            _data = data;
        }

        int& data()
        {
            return _data;
        }

        static int count()
        {
            return _count;
        }

    private:
        int _data;
        static int _count;
    };

    int TestObject::_count = 0;
}

struct SharedArrayTest
{

    void testSharedArray()
    {
        SharedArray<TestObject> ptr1;
        poco_assert(ptr1.get() == 0);
        TestObject* pTO1 = new TestObject[10];
        for(int i = 0; i < 10; i++){
            pTO1[i].set(i);
        }

        ptr1 = pTO1;
        assert (ptr1.get() == pTO1);
        assert (ptr1 == pTO1);
        for(int i = 0; i< 10; i++){
            assert(ptr1[i].data() == i);
        }

        TestObject* pTO2 = new TestObject[20];
        for(int i = 20; i > 0; i--){
            pTO2[i].set(i);
        }
        SharedArray<TestObject> ptr2 = pTO2;
        SharedArray<TestObject> ptr3 = ptr1;
        SharedArray<TestObject> ptr4;
        assert (ptr1.get() == pTO1);
        assert (ptr1 == pTO1);
        assert (ptr2.get() == pTO2);
        assert (ptr2 == pTO2);
        assert (ptr3.get() == pTO1);
        assert (ptr3 == pTO1);

        assert (ptr1 == pTO1);
        assert (ptr1 != pTO2);
        assert (ptr1 < pTO2);
        assert (ptr1 <= pTO2);
        assert (ptr2 > pTO1);
        assert (ptr2 >= pTO1);

        assert (ptr1 == ptr3);
        assert (ptr1 != ptr2);
        assert (ptr1 < ptr2);
        assert (ptr1 <= ptr2);
        assert (ptr2 > ptr1);
        assert (ptr2 >= ptr1);

        ptr1.swap(ptr2);
        assert (ptr2 < ptr1);
        ptr2.swap(ptr1);

        try
        {
            assert (ptr4[0].data() == 1);
            assert (!"must throw NullPointerException");
        }
        catch (NullPointerException&)
        {
        }

        assert (!(ptr4 == ptr1));
        assert (!(ptr4 == ptr2));
        assert (ptr4 != ptr1);
        assert (ptr4 != ptr2);

        ptr4 = ptr2;
        assert (ptr4 == ptr2);
        assert (!(ptr4 != ptr2));

        assert (TestObject::count() == 30);
        ptr1 = 0;
        ptr2 = 0;
        ptr3 = 0;
        ptr4 = 0;
        assert (TestObject::count() == 0);

        {
            SharedArray<TestObject> ptr = new TestObject[2];
            assert (TestObject::count() == 2);
        }
        assert (TestObject::count() == 0);

    }

};

int main()
{

    SharedArrayTest test;
    test.testSharedArray();
}
